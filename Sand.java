class Sand {
	
	static String sandType;
	static String sandColor;
	static String sandArea;
	
	//setter and getter
	
	static void setSandTypes(String type){
		sandType = type;
	}
	static String getSandType(){
		return sandType;
	}
	static void setSandColor(String co){
		sandColor = co;
	}
	static String getSandColor(){
		return sandColor;
	}
	static void setSandArea(String location){
		sandArea = location;
	}
	static String getSandArea(){
		return sandArea;
	}
}